public class Coordonnees {
    public final int x;
    public final int y;

    /* Crée un couple de coordonnées de la forme (x,y). */
    public Coordonnees(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

}
